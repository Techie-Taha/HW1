/*
Write a C++ program that calculates sum of pair values above
*/

#include <iostream>
using namespace std;
int Sum(int X, int Y){

    return X + Y;

}

int main(){
    int result1, result2;

    result1 = Sum(020, 0X60);
    result2 = Sum(0XFE, 0XABC);

    cout << "sum of pair values 020 and 0X60 are:  " << result1 << endl;
    cout << "sum of pair values 0XFE and 0XABC are:  " << result2 << endl;
    return 0;


}